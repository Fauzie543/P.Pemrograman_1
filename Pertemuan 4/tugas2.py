print("Berikut Tugas Kalkulator Sederhana ")

a = input ("Masukkan nilai a = ")
b = input ("Masukkan nilai b = ")


pertambahan = (int(a) + int (b))
print("Hasil dari a + b = " + str(pertambahan)) 

pengurangan = (int(a) - int (b))
print("Hasil dari a - b = " + str(pengurangan)) 

perkalian = (int(a) * int (b))
print("Hasil dari a * b = " + str(perkalian))

pembagian = (int(a) / int (b))
print("Hasil dari a / b = " + str(pembagian)) 

modulus = (int(a) % int (b))
print("Hasil dari a % b = " + str(modulus)) 

print("Sekian hasil kalkulator sederhana, terima kasih.") 